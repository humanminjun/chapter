package p695;

public class Student {
	private String name;
	private int english;
	private int mathScore;
	public Student(String name, int english, int mathScore) {
		super();
		this.name = name;
		this.english = english;
		this.mathScore = mathScore;
	}
	public String getName() {return name;}
	public int getEnglishScore() {return english;}
	public int getMathScore() {return mathScore;}
	
	
	
}
