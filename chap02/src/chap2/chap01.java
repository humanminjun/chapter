// 실행문과 세미콜론(;)
package chap2;

public class chap01 {
	public static void main(String[]args) {
		int x = 5;   //변수 x를 선언하고 1을 저장
		int y = 2;    //변수 y를 선언하고 2를 저장
        int result = x + y;   // 변수result 를 선언하고 x와 y를 더한 값을 저장
        
        System.out.println(x + "+" + y + "=" + result);   //콘솔에 출력하는 메소드 호출
        
	}

}